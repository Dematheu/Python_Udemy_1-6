# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. 
# int sem sinal é considerado Positivo

print(11) # int
print(-11) # int
print(0) # int
print()

# float -> Número com ponto flutuante (numeros com casas decimais)
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1, 10.12) # float
print(0.0) # float
print(-1.5) #float
print()

# A função type mostra o tipo que o Python
# inferiu ao valor.
print(type('Lucas'))
print(type(10), type(0), type(-1))
print(type(10.1), type(-1.1), type(0.0))